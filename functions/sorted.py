#this function can be used in any collection data type but the output is in list
s='arnab kumar sahu'
l=['srinibash','arnab','ashirbad','mahesh','ankita']
t=(10,23,445,46,77,5,34,6,36,)
print(sorted(s))#ascending order
print(sorted(l))
print(sorted(t))

print(sorted(s,reverse=True))#descending order
print(sorted(l,reverse=True))
print(sorted(t,reverse=True))
